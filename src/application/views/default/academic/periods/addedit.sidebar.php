<dl>
	<dt>Time format</dt>
	<dd>The supported formats for the Start time and End time fields are <span>HH:MM</span>, for example, <em>09:20</em>. You must also include the leading zero for hours 1-9.</dd>
	<dd>All times should also be entered in the <span>24 hour</span> format and be between 00:00 and 23:59.</dd>
</dl>